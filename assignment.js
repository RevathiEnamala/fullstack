
let score = 10;
console.log("Initial score:", score); 
score += 5;
console.log("After += 5:", score); 
score -= 3;
console.log("After -= 3:", score); 
score *= 2;
console.log("After *= 2:", score); 
score /= 4;
console.log("After /= 4:", score); 
score %= 4;
console.log("After %= 4 (remainder):", score); 
score **= 3;
console.log("After **= 3 (2^3):", score); 
